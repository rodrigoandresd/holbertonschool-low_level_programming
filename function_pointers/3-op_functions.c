#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - add
 * @a: integer
 * @b: integer
 * Return: operation
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - substract
 * @a: integer
 * @b: integer
 * Return: operation
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiplication
 * @a: integer
 * @b: integer
 * Return: operation
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divition
 * @a: integer
 * @b: integer
 * Return: operation
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - modulus
 * @a: integer
 * @b: integer
 * Return: operation
 */

int op_mod(int a, int b)
{
return (a % b);
}

